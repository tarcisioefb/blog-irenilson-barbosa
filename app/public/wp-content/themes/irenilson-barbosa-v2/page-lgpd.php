<h2>7. Direitos do titular (Art. 18 da LGPD)</h2>
<p>Como titular de dados pessoais, você tem os seguintes direitos, exercíveis mediante requisição expressa:</p>
<ol>
<li><strong>Confirmação e acesso:</strong> confirmar a existência de tratamento e acessar seus dados pessoais.</li>
<li><strong>Correção:</strong> corrigir dados incompletos, inexatos ou desatualizados.</li>
<li><strong>Eliminação:</strong> solicitar a exclusão dos dados pessoais tratados com base no consentimento.</li>
<li><strong>Revogação do consentimento:</strong> revogar o consentimento a qualquer momento, sem prejuízo da legalidade do tratamento anterior.</li>
<li><strong>Portabilidade:</strong> solicitar a portabilidade dos dados a outro fornecedor de serviço ou produto.</li>
<li><strong>Informação:</strong> ser informado sobre a possibilidade de não fornecer o consentimento e sobre as consequências da negativa.</li>
<li><strong>Oposição:</strong> opor-se ao tratamento realizado com base em interesse legítimo.</li>
</ol>
<p><strong>Como exercer seus direitos:</strong> envie um e-mail para <a href="mailto:contato@irenilsonbarbosa.com">contato@irenilsonbarbosa.com</a> ou utilize o <a href="/contato/">formulário de contato</a> com o assunto "LGPD — Direitos do titular". Responderemos em até <strong>15 dias</strong> conforme o Art. 19 da LGPD.</p>
<p>O exercício dos direitos é <strong>gratuito</strong>, salvo se a requisição for excessiva ou infundada, caso em que poderá ser cobrado custo administrativo razoável.</p>

<h2>8. Segurança dos dados</h2>
<p>Adotamos medidas técnicas e organizacionais para proteger seus dados pessoais contra acessos não autorizados, destruição, perda, alteração ou qualquer forma de tratamento inadequado:</p>
<ul>
<li><strong>Criptografia:</strong> todas as conexões com o site utilizam HTTPS/TLS (SSL), garantindo que os dados trafeguem de forma segura entre seu navegador e nossos servidores.</li>
<li><strong>Armazenamento seguro:</strong> os dados estão armazenados em servidores com acesso restrito, protegidos por firewalls e sistemas de detecção de intrusão.</li>
<li><strong>Senhas:</strong> todas as senhas são armazenadas utilizando hashing criptográfico forte (bcrypt).</li>
<li><strong>Acesso mínimo:</strong> apenas o controlador e o provedor de hospedagem (Hostinger) têm acesso aos dados armazenados.</li>
<li><strong>Backup:</strong> os dados são submetidos a backups regulares para garantir a disponibilidade e integridade.</li>
</ul>

<h2>9. Retenção e descarte dos dados</h2>
<p>Mantemos seus dados pessoais apenas pelo tempo necessário para cumprir as finalidades para as quais foram coletados:</p>
<ul>
<li><strong>Formulário de contato:</strong> os dados são mantidos até o completo atendimento da solicitação e, no máximo, 90 dias após o último contato, quando são eliminados.</li>
<li><strong>Newsletter:</strong> os dados são mantidos até que o usuário cancele a assinatura ou revogue o consentimento. O descarte é imediato após a solicitação.</li>
<li><strong>Comentários:</strong> os dados são mantidos enquanto o artigo estiver publicado e visível no site. A exclusão pode ser solicitada a qualquer momento.</li>
<li><strong>Google Analytics:</strong> os dados são retidos pelo período máximo de 26 meses, conforme a política de retenção do Google.</li>
</ul>

