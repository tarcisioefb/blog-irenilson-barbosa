<h2>2. Definições</h2>
<p>Para os fins desta Política, entende-se por:</p>
<ul>
<li><strong>Dado pessoal:</strong> qualquer informação relacionada a pessoa natural identificada ou identificável (Art. 5º, I da LGPD).</li>
<li><strong>Tratamento:</strong> toda operação realizada com dados pessoais, como coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão, distribuição, processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da informação, modificação, comunicação, transferência, difusão ou extração (Art. 5º, X da LGPD).</li>
<li><strong>Titular:</strong> pessoa natural a quem se referem os dados pessoais que são objeto de tratamento.</li>
<li><strong>Consentimento:</strong> manifestação livre, informada e inequívoca pela qual o titular concorda com o tratamento de seus dados pessoais para uma finalidade determinada (Art. 5º, XII da LGPD).</li>
</ul>

